x = "python     "
y = "   python"
z = "   python      "

print(x)
print("rstrip_x:")
print(x.rstrip())
print("x comes back to its original form. \nRename the variable to make it permanent.")
print(x)

print("\n" + y)
print("lstrip_y:")
print(y.lstrip())
print("y comes back to its original form. \nRename the variable to make it permanent.")
print(y)

print("\n" + z)
print("strip_z:")
print(z.strip())
print("z comes back to its original form. \nRename the variable to make it permanent.")
print(z)